/* nutation_iau2000a.c — full IAU 2000A nutation pipeline.
 *
 * The IAU 2000A model is the full 1378-term lunisolar + planetary
 * nutation series, accurate to ~0.2 mas (microarcsecond at extreme
 * accuracy budgets). The full table comes from IERS Conventions
 * (2010) Tables 5.3a (678 lunisolar terms) + 5.3b (687 planetary
 * terms) — too large to inline here in human-edited form without
 * substantial transcription risk.
 *
 * v0.1 strategy:
 *   1. Implement the full evaluator structure — operates on a
 *      runtime-loadable table.
 *   2. Bake in the largest ~80 terms (the IAU 2000B truncation
 *      from nutation_iau2006_short.c, replayed here as the starting
 *      table). These give ~1 mas accuracy.
 *   3. Provide k26astro_nutation_load_table(path) that ingests the
 *      full 1378-term tables from a binary file at
 *      /usr/share/k26astro/iers/iau2000a.dat — generated by the
 *      `tools/gen_iau2000a` helper from IERS-published ASCII.
 *
 * This gives K26 a working pipeline with reasonable accuracy out of
 * the box, plus a clean upgrade path to full IAU 2000A precision
 * when the IERS data file is installed.
 *
 * Source: IERS Conventions (2010), Chapter 5.6, §5.5.3-5.5.4.
 */
