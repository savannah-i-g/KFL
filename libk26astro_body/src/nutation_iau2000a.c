/* ---- Term structure --------------------------------------- *
 *
 * Full IAU 2000A term: 14 numbers per row.
 *
 * Lunisolar (Table 5.3a, 678 rows):
 *   l, l', F, D, Ω  (5 Delaunay multipliers)
 *   A_ψ, A'_ψ, A''_ψ                 (sin amplitude + linear-T + cos)
 *   A_ε, A'_ε, A''_ε                 (cos amplitude + linear-T + sin)
 *
 * Planetary (Table 5.3b, 687 rows): 14 multipliers (Mercury..Neptune
 * mean longitudes, Earth's longitude, GMST'97-related arguments) +
 * smaller coefficient set. K26 lumps both into one structure with
 * `kind` = LUNISOLAR or PLANETARY; the evaluator branches on it. */

typedef enum {
    K26_NUT_KIND_LUNISOLAR = 0,
    K26_NUT_KIND_PLANETARY = 1
} K26NutTermKind;

typedef struct {
    K26NutTermKind kind;
    /* Multipliers — for lunisolar use only the first 5; planetary
     * uses all 14 (8 planet mean longitudes, l, F, D, Ω + 1 extra). */
    int8_t  mult[14];
    double  A;     /* sin amplitude for Δψ */
    double  A_t;   /* linear-T term for Δψ sin */
    double  B;     /* cos amplitude for Δψ */
    double  C;     /* cos amplitude for Δε */
    double  C_t;   /* linear-T term for Δε cos */
    double  D;     /* sin amplitude for Δε */
} K26NutTerm;

