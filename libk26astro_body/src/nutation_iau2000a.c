/* ---- Active table (default → loaded) ---------------------- */

static const K26NutTerm *g_terms       = DEFAULT_TERMS;
static int               g_n_terms     = (int)(sizeof(DEFAULT_TERMS) / sizeof(DEFAULT_TERMS[0]));
static K26NutTerm       *g_owned_table = NULL;

/* ---- Loader ---------------------------------------------- *
 *
 * Binary file format (`/usr/share/k26astro/iers/iau2000a.dat`):
 *
 *   header
 *     u32  magic  = 'K26N'  (0x4E36324B in little-endian byte order)
 *     u32  version = 1
 *     u32  n_terms (lunisolar + planetary, both kinds)
 *     u32  reserved (0)
 *   records[n_terms]    each = sizeof(K26NutTerm) bytes (114B)
 *
 * Endian: little-endian only (target platforms are all x86_64 /
 * aarch64 in K26's set; portability shim can be added when needed).
 */
#define K26_NUT_FILE_MAGIC      0x4E36324Bu    /* 'K' '2' '6' 'N' in LE order */
#define K26_NUT_FILE_VERSION    1u

int k26astro_nutation_load_table(const char *path);

int k26astro_nutation_load_table(const char *path)
{
    if (!path) return 1;
    int fd = open(path, O_RDONLY);
    if (fd < 0) return 2;
    struct stat st;
    if (fstat(fd, &st) != 0) { close(fd); return 3; }
    if (st.st_size < 16) { close(fd); return 4; }
    void *map = mmap(NULL, (size_t)st.st_size, PROT_READ, MAP_PRIVATE, fd, 0);
    close(fd);
    if (map == MAP_FAILED) return 5;

    uint32_t magic, version, n_terms;
    memcpy(&magic,   (uint8_t *)map + 0, 4);
    memcpy(&version, (uint8_t *)map + 4, 4);
    memcpy(&n_terms, (uint8_t *)map + 8, 4);
    if (magic != K26_NUT_FILE_MAGIC) {
        munmap(map, (size_t)st.st_size); return 6;
    }
    if (version != K26_NUT_FILE_VERSION) {
        munmap(map, (size_t)st.st_size); return 7;
    }
    size_t needed = 16 + (size_t)n_terms * sizeof(K26NutTerm);
    if ((size_t)st.st_size < needed) {
        munmap(map, (size_t)st.st_size); return 8;
    }

    /* Copy out of the mapping into owned storage so the lifetime is
     * independent of the file mapping (caller may release it). */
    K26NutTerm *terms = (K26NutTerm *)malloc((size_t)n_terms * sizeof(K26NutTerm));
    if (!terms) {
        munmap(map, (size_t)st.st_size); return 9;
    }
    memcpy(terms, (uint8_t *)map + 16, (size_t)n_terms * sizeof(K26NutTerm));
    munmap(map, (size_t)st.st_size);

    /* Replace the active table. */
    if (g_owned_table) free(g_owned_table);
    g_owned_table = terms;
    g_terms       = terms;
    g_n_terms     = (int)n_terms;
    return 0;
}

void k26astro_nutation_unload_table(void);

void k26astro_nutation_unload_table(void)
{
    if (g_owned_table) {
        free(g_owned_table);
        g_owned_table = NULL;
    }
    g_terms   = DEFAULT_TERMS;
    g_n_terms = N_DEFAULT_TERMS;
}

/* ---- Planetary mean longitudes ------------------------- *
 *
 * IERS Conventions (2010) §5.7.1. Each in radians; argument of
 * planetary nutation terms is a linear combination of these. */

typedef struct {
    double Lme, Lve, Le, Lma, Lju, Lsa, Lur, Lne;  /* planet mean longitudes */
    double pa;                                       /* general precession in long. */
    double l, F, D, Om;                              /* shared Delaunay (minus l') */
    double extra;                                    /* not used by IAU 2000A; pad */
} PlanetaryArgs;

static PlanetaryArgs planetary_at_(double T)
{
    /* Planetary mean longitudes (Simon et al. 1994, IERS Conv. (2010)
     * eqs. 5.43). Linear in T. Arguments in radians. */
    PlanetaryArgs a;
    a.Lme = fmod(4.402608842    + 2608.7903141574    * T, K26A_TWO_PI);
    a.Lve = fmod(3.176146697    + 1021.3285546211    * T, K26A_TWO_PI);
    a.Le  = fmod(1.753470314    +  628.3075849991    * T, K26A_TWO_PI);
    a.Lma = fmod(6.203480913    +  334.0612426700    * T, K26A_TWO_PI);
    a.Lju = fmod(0.599546497    +   52.9690962641    * T, K26A_TWO_PI);
    a.Lsa = fmod(0.874016757    +   21.3299104960    * T, K26A_TWO_PI);
    a.Lur = fmod(5.481293872    +    7.4781598567    * T, K26A_TWO_PI);
    a.Lne = fmod(5.311886287    +    3.8133035638    * T, K26A_TWO_PI);
    a.pa  = fmod(0.024381750 * T + 0.00000538691 * T * T, K26A_TWO_PI);
    a.l   = fmod(2.355555898    + 8328.6914269554    * T, K26A_TWO_PI);
    a.F   = fmod(1.627905234    + 8433.4661581308    * T, K26A_TWO_PI);
    a.D   = fmod(5.198466741    + 7771.3771468121    * T, K26A_TWO_PI);
    a.Om  = fmod(2.182439196    -   33.7570459536    * T, K26A_TWO_PI);
    a.extra = 0.0;
    return a;
}

/* ---- Lunisolar Delaunay arguments — same as IAU 2000B ----- */
typedef struct { double l, lp, F, D, Om; } DelaunayArgs;

static DelaunayArgs delaunay_at_(double T)
{
    DelaunayArgs a;
    double as2r = K26A_RAD_PER_ARCSEC;
    a.l  = ( 485868.249036 + T * (1717915923.2178 + T * (31.8792)) ) * as2r;
    a.lp = (1287104.793048 + T * ( 129596581.0481 + T * (-0.5532)) ) * as2r;
    a.F  = ( 335779.526232 + T * (1739527262.8478 + T * (-12.7512)) ) * as2r;
    a.D  = (1072260.703692 + T * (1602961601.2090 + T * (-6.3706)) ) * as2r;
    a.Om = ( 450160.398036 + T * ( -6962890.5431 + T * (7.4722)) ) * as2r;
    double two_pi = K26A_TWO_PI;
    a.l  = fmod(a.l,  two_pi); if (a.l  < 0) a.l  += two_pi;
    a.lp = fmod(a.lp, two_pi); if (a.lp < 0) a.lp += two_pi;
    a.F  = fmod(a.F,  two_pi); if (a.F  < 0) a.F  += two_pi;
    a.D  = fmod(a.D,  two_pi); if (a.D  < 0) a.D  += two_pi;
    a.Om = fmod(a.Om, two_pi); if (a.Om < 0) a.Om += two_pi;
    return a;
}

static double epoch_to_T_tt_(const K26AstroEpoch *t)
{
    K26AstroEpoch tt = *t;
    if (tt.scale != K26A_TS_TT) k26astro_epoch_convert(&tt, K26A_TS_TT);
    double days = (double)tt.days_since_J2000 + tt.seconds_of_day / 86400.0;
    return days / 36525.0;
}

void k26astro_nutation_iau2000a(const K26AstroEpoch *t,
                                 double *dpsi_rad,
                                 double *deps_rad)
{
    if (!t) {
        if (dpsi_rad) *dpsi_rad = 0.0;
        if (deps_rad) *deps_rad = 0.0;
        return;
    }
    double T = epoch_to_T_tt_(t);
    DelaunayArgs DA = delaunay_at_(T);
    PlanetaryArgs PA = planetary_at_(T);

    double dpsi_01uas = 0.0;
    double deps_01uas = 0.0;
    for (int i = 0; i < g_n_terms; i++) {
        const K26NutTerm *tm = &g_terms[i];
        double arg;
        if (tm->kind == K26_NUT_KIND_LUNISOLAR) {
            arg = tm->mult[0] * DA.l
                + tm->mult[1] * DA.lp
                + tm->mult[2] * DA.F
                + tm->mult[3] * DA.D
                + tm->mult[4] * DA.Om;
        } else {
            /* Planetary: 14 multipliers as documented in IERS Conv.
             * 5.5.3. Layout: [Lme, Lve, Le, Lma, Lju, Lsa, Lur, Lne,
             * pa, l, F, D, Ω, 0]. Some publications use 8-planet or
             * 9-element variants; we use the 14-slot layout matching
             * IAU 2000A's published format. */
            arg = tm->mult[ 0] * PA.Lme
                + tm->mult[ 1] * PA.Lve
                + tm->mult[ 2] * PA.Le
                + tm->mult[ 3] * PA.Lma
                + tm->mult[ 4] * PA.Lju
                + tm->mult[ 5] * PA.Lsa
                + tm->mult[ 6] * PA.Lur
                + tm->mult[ 7] * PA.Lne
                + tm->mult[ 8] * PA.pa
                + tm->mult[ 9] * PA.l
                + tm->mult[10] * PA.F
                + tm->mult[11] * PA.D
                + tm->mult[12] * PA.Om;
        }
        double s = sin(arg), c = cos(arg);
        dpsi_01uas += (tm->A + tm->A_t * T) * s + tm->B * c;
        deps_01uas += (tm->C + tm->C_t * T) * c + tm->D * s;
    }
    const double scale_01uas_to_rad = 1.0e-7 * K26A_RAD_PER_ARCSEC;
    if (dpsi_rad) *dpsi_rad = dpsi_01uas * scale_01uas_to_rad;
    if (deps_rad) *deps_rad = deps_01uas * scale_01uas_to_rad;
}
